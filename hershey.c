//This is a standard implementation of Bresenham's line drawing algorithm
//modified from Java program at http://escience.anu.edu.au/lecture/cg/Line/BresenhamAlgo.en.html .
void gr_line(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1,uint32_t color,
		void (*setpoint)(uint16_t, uint16_t, uint32_t)){
	int16_t dx, dy, stepx, stepy, fraction;
	dy = y1 - y0;
	dx = x1 - x0;

	if (dy < 0) { dy = -dy;  stepy = -1; } else { stepy = 1; }
	if (dx < 0) { dx = -dx;  stepx = -1; } else { stepx = 1; }
	dy <<= 1;
	dx <<= 1;

	(*setpoint)(x0, y0, color);
	if (dx > dy) {
		fraction = dy - (dx >> 1);
		while (x0 != x1) {
			if (fraction >= 0) {
				y0 += stepy;
				fraction -= dx;
			}
			x0 += stepx;
			fraction += dy;
			(*setpoint)(x0, y0, color);
		}
	} else {
		fraction = dx - (dy >> 1);
		while (y0 != y1) {
			if (fraction >= 0) {
				x0 += stepx;
				fraction -= dy;
			}
			y0 += stepy;
			fraction += dx;
			(*setpoint)(x0, y0, color);
		}
	}
}

void placechar(char c, uint16_t* px, uint16_t* py, uint32_t color, uint8_t size,
		void (*setpoint)(uint16_t, uint16_t, uint32_t)) {
	uint16_t orgX, orgY, fptr;
	uint8_t pairs, i;
	int8_t lmargin, rmargin, curX, curY, prvX, prvY;
	orgX = *px;
	orgY = *py;
	fptr = Fontptr[c-32];
	pairs = Font[fptr];
	lmargin = Font[fptr+1]-'R';
	rmargin = Font[fptr+2]-'R';
	orgX -= lmargin*size/16;
	*px += (rmargin-lmargin)*size/16;
	fptr += 3;
	curX = 0;
	curY = 0;

	for (i = 0; i < (pairs-1)*2; i+=2) {
		prvX = curX;
		prvY = curY;
		curX = Font[fptr+i]-'R';
		curY = Font[fptr+i+1]-'R';

		if (i!=0) {
			if ((!((prvX==' '-'R') && (prvY=='R'-'R')))
					&& (!((curX==' '-'R') && (curY=='R'-'R')))) {
				gr_line((prvX*size)/16+orgX,
						(prvY*size)/16+orgY, (curX*size)/16+orgX,
						(curY*size)/16+orgY, color, setpoint);
			}
		}

	}
}

uint16_t get_char_width(char c, uint8_t size){
	uint16_t fptr;
	int8_t lmargin, rmargin;
	fptr = Fontptr[c-32];
	lmargin = Font[fptr+1]-'R';
	rmargin = Font[fptr+2]-'R';
	return (uint16_t)(rmargin-lmargin)*size/16;
}

uint16_t get_string_width(char* s, uint8_t size){
	uint8_t i;
	uint16_t w;
	w=0;
	for (i=0; s[i]!=0; i++) {
		w+=get_char_width(s[i], size);
	}
	return w;
}

void placestring(char* s, uint16_t* px, uint16_t* py, uint32_t color, uint8_t size,
		void (*setpoint)(uint16_t, uint16_t, uint32_t)) {
	uint8_t i;
	for (i=0; s[i]!=0; i++) {
		placechar(s[i], px, py, color, size, setpoint);
	}
}

//right justified string place
void placestring_rj(char* s, uint16_t* px, uint16_t* py, uint32_t color, uint8_t size,
		void (*setpoint)(uint16_t, uint16_t, uint32_t)){
	uint8_t i;
	int16_t total_x;

	total_x=0;
	for (i = 0; s[i] != 0; i++) {
		total_x+=get_char_width(s[i], size);
	}

	if ((*px)-total_x<0){
		return;
	}

	(*px)-=total_x;

	for (i = 0; s[i] != 0; i++) {
		placechar(s[i], px, py, color, size, setpoint);
	}

}


